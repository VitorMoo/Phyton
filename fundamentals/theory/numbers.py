from math import *
"""Numbers in phyton"""

# Addition, subtraction, multiplication
print(10 + 5)
print(3 * (4 + 3))

# Modulo (remainder)
print(10 % 3)


# Using variables
my_num = 10
print(str(my_num) + " is my favorite number")

# Absolute value
other_number = -45
print(abs(other_number))

# Exponential (power)
print(pow(3, 2))  # 3^2

# Maximum and minimum
print(max(4, 5, 10, 2))  # returns the bigger number
print(min(4, 6))  # returns the smallest number
# Rounding
print(round(3.2))  # rounds the number


# Advanced math functions from math import *
print(floor(3.6))  # chop off the decimal part
print(sqrt(36))  # square root

# Trigonometric functions
angle_rad = radians(45)  # Convert degrees to radians
print(sin(angle_rad))
print(cos(angle_rad))
print(tan(angle_rad))

# Logarithmic functions
print(log(10))  # Natural logarithm
print(log10(100))  # Base-10 logarithm

# Exponential and logarithmic relationships
print(exp(2))  # e^2
print(log(8, 2))  # Logarithm of 8 to the base 2

# Constants
print(pi)  # Pi (3.141592653589793)
print(e)   # Euler's number (2.718281828459045)
